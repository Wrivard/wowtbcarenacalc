// Generates data/abilityIcons.ts — a name → zamimg spell-icon map for the
// iconic TBC arena abilities that the comp guides name in prose. The icons
// are resolved from Wowhead's TBC spell tooltip endpoint (authoritative), not
// guessed, so none 404.
//
// Comp guides describe win conditions and cooldown timelines in text; players
// who have known these spells for 20 years recognise the icon far faster than
// the words. The comp page scans its guide text for these names and renders
// the matching icons.
//
// Re-run:  node scripts/build-ability-icons.mjs
// Add an ability: put its name + TBC spell id in ABILITIES and re-run.

