async function iconFor(id) {
  const res = await fetch(`https://nether.wowhead.com/tbc/tooltip/spell/${id}`);
  if (!res.ok) return null;
  const j = await res.json();
  return j.icon ? { icon: j.icon, name: j.name } : null;
}

const entries = [];
for (const [name, id] of Object.entries(ABILITIES)) {
  try {
    const hit = await iconFor(id);
    if (hit) {
      entries.push([name, hit.icon]);
      console.log("  %s -> %s (%s)", name, hit.icon, hit.name);
    } else {
      console.warn("  ! no icon for %s (%d)", name, id);
    }
  } catch (e) {
    console.warn("  ! fetch failed for %s (%d): %s", name, id, e.message);
  }
}

entries.sort((a, b) => a[0].localeCompare(b[0]));

const out = [
  "// GENERATED by scripts/build-ability-icons.mjs — do not edit by hand.",
  "//",
  "// Iconic TBC arena abilities → their zamimg spell-icon names, resolved from",
  "// Wowhead so none 404. The comp guides scan their prose for these names and",
  "// render the icon as a recognition anchor. Re-run the script to add more.",
  "",
  "export const ABILITY_ICONS: Record<string, string> = {",
  ...entries.map(([n, i]) => `  ${JSON.stringify(n)}: ${JSON.stringify(i)},`),
  "};",
  "",
];

fs.writeFileSync(path.join(ROOT, "data/abilityIcons.ts"), out.join("\n"), "utf8");
console.log("\ndata/abilityIcons.ts — %d abilities", entries.length);
